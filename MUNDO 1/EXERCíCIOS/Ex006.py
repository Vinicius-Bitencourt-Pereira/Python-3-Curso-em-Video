# ex-006 Escreva um programa que leia um numero e mostre seu dobro, tripulo e raiz quadrada.

# Criando a variável de entrada 'num' do tipo inteira.

num = int(input('Digite um numero: '))

# Dobro: num * 2
# Triplo: num * 3
# Raiz: num ** 0.5

print(f'Voce digitou {num}, seu dobro é {num * 2}, seu triplo é {num * 3} e sua raiz quadrada é {num ** (1/2):.2f} .')
                                                                                                 
