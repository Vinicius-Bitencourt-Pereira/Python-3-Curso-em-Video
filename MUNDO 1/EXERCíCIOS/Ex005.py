# ex-005 escreva um programa que leia um número inteiro e mostre na tela o seu antecessor e o seu sucesso.

# Criando a variável de entrada 'num' do tipo inteira.
num = int(input('Digite um número: '))

# antecessor: num - 1
# sucessor: num + 1

print(f'Voce digitou {num} , seu antecessor é {num - 1}, e seu sucessor é {num +1} . ')