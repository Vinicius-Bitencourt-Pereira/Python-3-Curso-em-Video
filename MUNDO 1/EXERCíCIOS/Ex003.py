# Ex-003 Crie um programa que leia dois numeros e mostre a soma entre eles.

# Criando as variaves de entrada n1 e n2 do tipo float ( Números c/ ponto flutualte ). Após é mostrado a soma dos mesmos.

n1 = float(input('Digite o primeiro numero:'))
n2 = float(input('Digite o segundo numero numero:'))
print(f'A soma entre {n1} + {n2} é: {n1+n2}')