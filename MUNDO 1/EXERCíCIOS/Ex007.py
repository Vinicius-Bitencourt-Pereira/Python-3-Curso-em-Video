# ex-007 Escreva um programa que leia duas notas de um aluno e mostre sua media.

# Criando as variáveis de entrada: nota1, nota2.
nota1 = float(input('Digite a primeira nota: '))
nota2 = float(input('Digite a segunda nota: '))

# Criando a variável que recebe o cálculo da média.
media = (nota1 + nota2) / 2

# Imprimindo o resultado.
print(f'A média entre suas notas é: {media} ')